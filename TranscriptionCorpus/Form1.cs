        private void button53_Click(object sender, EventArgs e) // transcription
        {
            string recu = label2.Text;
            string[] mise_en_forme;
            string mot_transcris = "";// mot transcris
            string mot_p_retenu = ""; // mot précédament retenue
            int cpt = 0;
            int cpt_max = 0;
            int cpt_MEN = 0;
            int cpt_MEN_max = 0;

            char coupure_space = ' ';
            char coupure = '>'; //séparation des mots 
            string[] reduction = recu.Split(coupure);



            cpt_max  = reduction.Length - 1;

            // remise en forme de la phrase

            while (cpt < cpt_max)
            {
                cpt++;
                mise_en_forme = reduction[cpt].Split(coupure_space);

                cpt_MEN_max = mise_en_forme.Length - 1;
                cpt_MEN = 0;
                mot_transcris = "";

                while (cpt_MEN < cpt_MEN_max)
                {
                    mot_transcris = mot_transcris + mise_en_forme[cpt_MEN];
                    cpt_MEN++;
                }


                mot_p_retenu = mot_p_retenu + " " + mot_transcris; 
           }
            label2.Text = "> " + mot_p_retenu;
        }
   
        private void button54_Click(object sender, EventArgs e) // suppression du dernier mots
        {
            string a_reduire = label2.Text;
            string temp = ">";
            char[] coupure = temp.ToCharArray();
            string backSpace = "";



            if (cptSpace == 0) // il y a 5 espace
            {
                backSpace = a_reduire.Split(coupure)[6];
                subLigne(backSpace, a_reduire);
            }
            else if (cptSpace == 1)
            {
                backSpace = a_reduire.Split(coupure)[5];
                subLigne(backSpace, a_reduire);
            }
            else if (cptSpace == 2) // il y a 3 espace
            {
                backSpace = a_reduire.Split(coupure)[4];
                subLigne(backSpace, a_reduire);
            }
            else if (cptSpace == 3)
            {
                backSpace = a_reduire.Split(coupure)[3];
                subLigne(backSpace, a_reduire);
            }
            else if (cptSpace == 4) // il y a 1 espace
            {
                backSpace = a_reduire.Split(coupure)[2];
                subLigne(backSpace, a_reduire);
            }
            else if (cptSpace == 5)
            {
                backSpace = a_reduire.Split(coupure)[1];
                subLigne(backSpace, a_reduire);
            }

            

            label2.Text = subbed;
            if (cptSpace < 5)
            {
                cptSpace++;
                button56.Text = "Faire un espace ( " + cptSpace + " espace restants )";
            }


            if (label2.Text.Length == 0)
            {
                label2.Text = ">";
            }
        }

